package unicom;

public class Pos {
	int i;
	int j;
	public Pos(int i, int j) {
		super();
		this.i = i;
		this.j = j;
	}
	public Pos()
	{
		
	}
	public int getI() {
		return i;
	}
	public void setI(int i) {
		this.i = i;
	}
	public int getJ() {
		return j;
	}
	public void setJ(int j) {
		this.j = j;
	}
	

}
